        <p>

<ol>
<li>
Dans le bol de votre robot (ou une jatte si vous la réalisez à la main) placez la farine, la levure émiettée, le sucre ainsi que les oeufs. Ajoutez le sel en dernier en prenant garde à ne pas le faire entrer en contact avec la levure.
    </li>
    <li>
    Pétrissez à vitesse moyenne pendant 5 minutes, jusqu'à ce que le mélange soit homogène.
    </li>
    <li>
    Coupez le beurre en morceaux et ajoutez-le à la pâte tout en pétrissant. Augmentez la vitesse de votre robot et laissez pétrir un gros quart d'heure. La pâte doit se décoller des bords du bol.
    </li>
    <li>
    Recouvrez avec un linge propre ou d'un film étirable et laissez lever pendant au moins 1 h dans un endroit tiède. Elle doit doubler de taille, cela peut prendre une heure ou plus.
    </li>
    <li>
    Travaillez à nouveau la pâte (dégazez-la) en y incorporant les pépites de chocolat. Façonnez-la en un gros boudin puis déposez-le dans un moule à cake d'environ 25 cm de long. Vous pouvez aussi préparer des biochettes en façonnant des boules avec la pâte. Recouvrez avec un linge et laissez lever à nouveau pendant au moins 1h. 
    </li>
    <li>
    Préchauffez le four à 170°C. Faites cuire pendant 15 min, puis laissez reposer 15 min environ hors du four avant de démouler. Cette brioche peut se servir froide ou tiède, selon votre goût.
    </li>
    
</ol>
       
