Pour réaliser cette Recette, vous aurez besoin des ingrédients suivants:

Pour la lasagne maison : <br>

    -300 g bœuf haché <br>
    -1/2 paquet de feuilles de lasagnes <br>
    -1 oignon <br>
    -1 gousse d’ail <br>
    -800 g de pulpe de tomate en conserve <br>
    -100 g de gruyère râpé <br>
    -50 g de parmesan râpé <br>
    -2 morceaux de sucre <br>
    -4 c. à soupe huile d’olive <br>
    -20 g de beurre <br>
    -1 c. à soupe d’herbes de provence <br>
    -sel, poivre <br>

Pour la sauce béchamel : <br>

    -70 g de farine <br>
    -50 cl de lait <br>
    -70 g de beurre <br>
    -1 pincée de noix de muscade
    -sel, poivre

</p>
