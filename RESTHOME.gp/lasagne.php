        <p>
    Priorité à la garniture des lasagnes : pelez l’oignon et la gousse d’ail, et émincez-les finement, puis faites-les revenir à feu doux dans une casserole avec 2 c. à soupe d’huile d’olive, jusqu’à ce que les oignons deviennent un peu translucides. 
    Ajoutez ensuite la pulpe de tomate, le sucre et les herbes de provence. 
    Salez, poivrez, puis laissez réduire et mijoter à feu doux pendant 20 min.
    Pendant la cuisson de la sauce tomate, faites revenir le boeuf dans une poêle avec 2 c. à soupe l’huile d’olive, pendant 3 à 5 min à feu moyen.
    Salez et poivrez, puis mélangez à la sauce tomate et attendez la fin de la cuisson.
    Pour préparer la sauce béchamel, faites d’abord fondre le beurre dans une casserole, puis ajoutez la farine hors du feu et mélangez le tout au fouet.
    Ajoutez le lait, fouettez énergiquement pour éviter la formation de grumeaux.
    Replacez sur feu doux et laissez épaissir quelques minutes, tout en mélangeant régulièrement.
    Salez, poivrez et parfumez avec la noix de muscade.
    Quand la garniture et la béchamel sont prêtes, préchauffez le four à 180°C (th. 6) le temps de “monter” votre lasagne maison.
    Graissez les parois d’un plat à gratin avec le beurre fondu, puis déposez dans le fond une couche de feuilles de pâte à lasagne et nappez-la de sauce béchamel.
    Ajoutez par dessus une couche de garniture à la sauce tomate et au boeuf haché, puis parsemez de gruyère râpé.
    Répétez l’opération jusqu’à épuisement des ingrédients, en terminant par une couche de béchamel que vous n’oublierez pas de couvrir d’une belle couche de parmesan râpé.
    Enfournez votre lasagne maison pendant 45 min le temps de la laisser cuire et gratiner, puis laissez-la refroidir 5 à 10 min avant de la servir en belles portions rectangulaires.
