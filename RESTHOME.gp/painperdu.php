<ol>
    <li>
    Commencez par prélever les graines de la gousse de vanille fendue, et faites chauffer avec le lait, aux premiers bouillons, enlevez du feu et laissez infuser jusqu’à ce que le lait soit tiède
    </li>
    <li>
    Fouettez les oeufs avec le sucre
    </li>
    <li>
    Ajoutez le lait vanillé au mélange
    </li>
    <li>
    Découpez de belles tranches de pain. Je préconise vraiment de faire des tranches de 3 cm de large
    </li>
    <li>
    Faites tremper les tranches dans la préparation, au moins 30 secondes en les retournant. Cela va permettre au pain de s’imprégner jusqu’au coeur. Ensuite égouttez vos tranches
    </li>
    <li>
    Roulez les rapidement dans un peu de sucre. C’est une astuces pour « caraméliser » le pain perdu ensuite, ça apporte du croustillant à votre pain perdu
    </li>
    <li>
    Faites chauffer une noix de beurre dans une poêle, et faites revenir les tranches de pain en les retournant, au moins 3 minutes, elles doivent être dorées
    </li>
    <li>
    Servez les chaudes, avec pourquoi pas des fruits ou une boule de glace vanille
    </li>
    
</ol>
       
